# PyVM 二进制文件功能演示
print("PyVM Binary File Demo")

# 变量操作
message = "Hello Binary World"
number = 42
flag = True

print(message)
print(number)
print(flag)

# 算术运算
x = 10
y = 20
sum_val = x + y
product = x * y

print(sum_val)
print(product)

# 条件判断
if sum_val > 25:
    print("Sum is large")

if product > 100:
    print("Product is very large")

# 更多计算
final_result = sum_val + product * 2
print(final_result)

print("Demo Complete")
